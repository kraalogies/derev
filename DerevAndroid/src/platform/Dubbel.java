package platform;

public class Dubbel {
	private final Object eerste;
	private final Object tweede;
	public Dubbel(Object eerste, Object tweede) {
		super();
		this.eerste = eerste;
		this.tweede = tweede;
	}
	public Object kryEerste() {
		return eerste;
	}
	public Object kryTweede() {
		return tweede;
	}
	

}
